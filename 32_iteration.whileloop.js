// While

let index = 0
while (index <= 10) {
    console.log(`Value of index is ${index}`);
    index = index + 2
}

let array = ["flash", "batman", "superman"]
let arr = 0
while (arr < array.length) {
    console.log(`Value is ${array[arr]}`);
    arr++
}

let score = 11
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);